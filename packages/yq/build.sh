TERMUX_PKG_MAINTAINER="@termux"
TERMUX_PKG_VERSION="4.48.2"
TERMUX_PKG_SRCURL=https://github.com/mikefarah/yq/archive/refs/tags/v${TERMUX_PKG_VERSION}.tar.gz
